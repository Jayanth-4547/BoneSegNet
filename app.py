# --------------------------------------------------
# 4. CLINICAL PDF GENERATOR (SEGMENTATION FOCUSED)
# --------------------------------------------------
def create_pdf(p_data, res, scan_tensor, slices):
    scan_tensor = scan_tensor.cpu().detach()
    d_max, h_max, w_max = scan_tensor.shape[2], scan_tensor.shape[3], scan_tensor.shape[4]
    
    safe_slices = [
        min(slices[0], d_max - 1), # Axial Slice Index
        min(slices[1], h_max - 1), # Coronal Slice Index
        min(slices[2], w_max - 1)  # Sagittal Slice Index
    ]
    
    class ClinicalReport(FPDF):
        def header(self):
            self.rect(5, 5, 200, 287)
            self.set_font('Arial', 'B', 16)
            self.cell(0, 10, 'BoneSegNet Radiology Report', 0, 1, 'C')
            self.set_font('Arial', 'I', 10)
            self.cell(0, 5, '2.5D AI-Assisted Osteology Segmentation System', 0, 1, 'C')
            self.line(10, 25, 200, 25)
            self.ln(15)
        def footer(self):
            self.set_y(-20)
            self.set_font('Arial', 'I', 8)
            self.set_text_color(128)
            self.cell(0, 10, f'Page {self.page_no()} | Generated by BoneSegNet Triage', 0, 0, 'C')

    def save_view(tensor, mask_vol, slice_idx, axis_id, title):
        # CLINICAL STANDARD ROTATIONS: .T (transpose) + flipud (flip upright)
        if axis_id == 0:   # Axial (Top-Down)
            img = tensor[0, 0, slice_idx, :, :].numpy()
            mask = mask_vol[slice_idx, :, :] if mask_vol is not None else None
        elif axis_id == 1: # Coronal (Front)
            img = np.flipud(tensor[0, 0, :, slice_idx, :].numpy().T)
            mask = np.flipud(mask_vol[:, slice_idx, :].T) if mask_vol is not None else None
        else:              # Sagittal (Side)
            img = np.flipud(tensor[0, 0, :, :, slice_idx].numpy().T)
            mask = np.flipud(mask_vol[:, :, slice_idx].T) if mask_vol is not None else None

        fig, ax = plt.subplots(1, 2, figsize=(8, 4))
        plt.subplots_adjust(wspace=0.1)

        ax[0].imshow(img, cmap="gray", aspect='auto')
        ax[0].set_title(f"{title} - Raw Window", fontsize=10)
        ax[0].axis('off')

        ax[1].imshow(img, cmap="gray", aspect='auto')
        if mask is not None and mask.max() > 0:
            # Alpha 1.0 + vmin/vmax for perfect solid green visibility
            masked_data = np.ma.masked_where(mask < 0.5, mask)
            ax[1].imshow(masked_data, cmap=green_cmap, alpha=1.0, aspect='auto', vmin=0, vmax=1)
        
        ax[1].set_title(f"{title} - AI Mask", fontsize=10)
        ax[1].axis('off')

        fd, path = tempfile.mkstemp(suffix=".png")
        os.close(fd)
        plt.savefig(path, bbox_inches='tight', dpi=150)
        plt.close(fig)
        return path

    pdf = ClinicalReport()
    pdf.set_auto_page_break(auto=True, margin=20)

    # PAGE 1
    pdf.add_page()
    pdf.set_font("Arial", 'B', 12)
    pdf.set_fill_color(240, 240, 240)
    pdf.cell(0, 8, "  1. Patient Demographics", 0, 1, 'L', fill=True)
    pdf.ln(4)
    
    pdf.set_font("Arial", size=10)
    pdf.cell(50, 6, f"Patient ID: {p_data['id']}", 0, 0)
    pdf.cell(0, 6, f"Scan Date: {p_data['date']}", 0, 1)
    pdf.ln(5)

    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 8, "  2. Segmentation Assessment", 0, 1, 'L', fill=True)
    pdf.ln(4)
    
    is_pos = res['affected_slices'] > 0
    color = (200, 50, 50) if is_pos else (50, 150, 50)

    pdf.set_font("Arial", 'B', 11)
    pdf.cell(45, 6, "AI Output:", 0, 0)
    pdf.set_text_color(*color)
    pdf.cell(0, 6, res['diagnosis'].upper(), 0, 1)
    pdf.set_text_color(0)
    
    pdf.set_font("Arial", size=10)
    pdf.cell(45, 6, "Total Affected Slices:", 0, 0)
    pdf.cell(0, 6, str(res['affected_slices']), 0, 1)
    pdf.cell(45, 6, "Lesion Volume (px):", 0, 0)
    pdf.cell(0, 6, str(res['lesion_volume_px']), 0, 1)
    pdf.ln(5)

    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 8, "  3. Primary View (Axial Overlay)", 0, 1, 'L', fill=True)
    pdf.ln(2)

    axial_path = save_view(scan_tensor, res['heatmap'], safe_slices[0], 0, "Axial View")
    pdf.image(axial_path, x=20, w=170)
    try: os.remove(axial_path)
    except: pass

    # PAGE 2 (Views)
    pdf.add_page()
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 8, "  4. Multi-Planar Segmentation", 0, 1, 'L', fill=True)
    pdf.ln(6)

    cor_path = save_view(scan_tensor, res['heatmap'], safe_slices[1], 1, "Coronal View")
    pdf.image(cor_path, x=25, w=160)
    try: os.remove(cor_path)
    except: pass
    pdf.ln(6)

    sag_path = save_view(scan_tensor, res['heatmap'], safe_slices[2], 2, "Sagittal View")
    pdf.image(sag_path, x=25, w=160)
    try: os.remove(sag_path)
    except: pass

    return pdf.output(dest='S').encode('latin-1')

