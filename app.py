# --- 6. MAIN PAGE ---
st.title("🦴 2.5D Volumetric Bone Segmentation")

uploaded_file = st.file_uploader("Upload Bone Windowed CT (.pt)", type=["pt"], label_visibility="collapsed")

if uploaded_file is not None:
    scan_tensor = torch.load(uploaded_file, map_location='cpu', weights_only=False)
    if scan_tensor.dim() == 3: scan_tensor = scan_tensor.unsqueeze(0).unsqueeze(0)
    elif scan_tensor.dim() == 4: scan_tensor = scan_tensor.unsqueeze(0)
    
    st.session_state.scan_tensor = scan_tensor

    st.subheader("1. Pre-Inference Scan Verification")
    with st.expander("Show Quality Check", expanded=True):
        d_mid, h_mid, w_mid = scan_tensor.shape[2]//2, scan_tensor.shape[3]//2, scan_tensor.shape[4]//2
        c1, c2, c3 = st.columns(3)
        
        # CLINICAL STANDARD ROTATIONS: .T (transpose) + flipud (flip upright)
        img_ax = scan_tensor[0, 0, d_mid, :, :].numpy()
        img_cor = np.flipud(scan_tensor[0, 0, :, h_mid, :].numpy().T)
        img_sag = np.flipud(scan_tensor[0, 0, :, :, w_mid].numpy().T)
        
        c1.image(img_ax, caption="Axial Window", clamp=True, use_container_width=True)
        c2.image(img_cor, caption="Coronal Window", clamp=True, use_container_width=True)
        c3.image(img_sag, caption="Sagittal Window", clamp=True, use_container_width=True)
        
    st.divider()
    col_run, _ = st.columns([1, 2])
    with col_run:
        if st.button("RUN 2.5D SEGMENTATION", type="primary"):
            with st.spinner("Extracting spatial features across Z-axis..."):
                if BACKEND_LOADED:
                    my_bar = st.progress(0, text="Initializing tensors...")
                    st.session_state.results = system.analyze_scan(scan_tensor, progress_bar=my_bar)
                    my_bar.empty()
                else:
                    st.error("Backend offline")

    if 'results' in st.session_state and st.session_state.results:
        res = st.session_state.results
        st.divider()
        st.subheader("2. Segmentation Metrics")
        
        # 4 Columns to fit the new Confidence Score
        k1, k2, k3, k4 = st.columns(4)
        is_pos = res['affected_slices'] > 0
        
        k1.metric("Clinical Assessment", res['diagnosis'], delta="Lesion Detected" if is_pos else "Clear", delta_color="inverse" if is_pos else "normal")
        k2.metric("Slices Affected", res['affected_slices'])
        k3.metric("Tumor Volume (px)", res['lesion_volume_px'])
        
        conf_text = f"{res['confidence']:.1f}%" if is_pos else "N/A"
        k4.metric("AI Confidence", conf_text)

        st.divider()
        st.subheader("3. Pixel-Level Boundary Localization")
        
        d_size, h_size, w_size = scan_tensor.shape[2], scan_tensor.shape[3], scan_tensor.shape[4]
        c_left, c_mid, c_right = st.columns(3)

        # --- AUTO-TARGETING LOGIC ---
        heatmap = res['heatmap']
        
        # Default to the middle of the scan if no tumor is found
        best_ax, best_cor, best_sag = d_size // 2, h_size // 2, w_size // 2
        
        # If the AI found a tumor, find the slice with the highest pixel density
        if heatmap is not None and heatmap.max() > 0:
            best_ax = int(np.argmax(np.sum(heatmap, axis=(1, 2))))  # Axis 0
            best_cor = int(np.argmax(np.sum(heatmap, axis=(0, 2)))) # Axis 1
            best_sag = int(np.argmax(np.sum(heatmap, axis=(0, 1)))) # Axis 2

        # Sliders now instantly snap to the worst part of the tumor
        ax_sl = c_left.slider("Axial (Axis 0)", 0, d_size-1, best_ax)
        cor_sl = c_mid.slider("Coronal (Axis 1)", 0, h_size-1, best_cor)
        sag_sl = c_right.slider("Sagittal (Axis 2)", 0, w_size-1, best_sag)

        # CLINICAL STANDARD ROTATIONS FOR SLIDERS
        img_ax = scan_tensor[0, 0, ax_sl, :, :].numpy()
        mask_ax = heatmap[ax_sl, :, :] if heatmap is not None else None
        
        img_cor = np.flipud(scan_tensor[0, 0, :, cor_sl, :].numpy().T)
        mask_cor = np.flipud(heatmap[:, cor_sl, :].T) if heatmap is not None else None
        
        img_sag = np.flipud(scan_tensor[0, 0, :, :, sag_sl].numpy().T)
        mask_sag = np.flipud(heatmap[:, :, sag_sl].T) if heatmap is not None else None

        fig, ax = plt.subplots(1, 3, figsize=(15, 5))
        plt.subplots_adjust(wspace=0.1, left=0, right=1, top=1, bottom=0)
        
        def plot_sub(axis, img, mask, title):
            axis.imshow(img, cmap="gray", aspect='auto')
            if mask is not None and mask.max() > 0:
                masked_overlay = np.ma.masked_where(mask < 0.5, mask)
                # vmin=0, vmax=1 forces the transparent-to-green colormap to render properly
                axis.imshow(masked_overlay, cmap=green_cmap, alpha=1.0, aspect='auto', vmin=0, vmax=1)
            axis.set_title(title, fontsize=12, fontweight='bold', pad=10)
            axis.axis('off')
            
        plot_sub(ax[0], img_ax, mask_ax, f"Axial (Slice {ax_sl})")
        plot_sub(ax[1], img_cor, mask_cor, f"Coronal (Slice {cor_sl})")
        plot_sub(ax[2], img_sag, mask_sag, f"Sagittal (Slice {sag_sl})")
        
        st.pyplot(fig, use_container_width=True)

        st.divider()
        st.subheader("📄 Automated Triage Documentation")
        c1, c2 = st.columns([3, 2], gap="large")

        with c1:
            st.markdown("### 🩺 Clinical Action")
            if is_pos:
                st.error("### ⚠ Lytic Lesion Geometry Detected")
                st.markdown("**System Flags:** Strict boundary detection confirmed within Hounsfield bone window. High probability of multiple myeloma or osteosarcoma. Recommend immediate verification of green mask overlays by attending radiologist.")
            else:
                st.success("### ✅ No Boundary Disruption Detected")
                st.markdown("**System Flags:** Trabecular and cortical bone structures appear continuous. No lytic cavities flagged by U-Net++ decoder.")

        with c2:
            st.markdown("### 📑 Export PDF Report")
            with st.container(border=True):
                # Pass the slices in the exact order [Axial, Coronal, Sagittal]
                pdf_bytes = create_pdf({"id": patient_id, "date": scan_date}, res, scan_tensor, [ax_sl, cor_sl, sag_sl])
                st.download_button(
                    label="Download Visual Autopsy (PDF)",
                    data=pdf_bytes,
                    file_name=f"BoneSegNet_{patient_id}.pdf",
                    mime="application/pdf",
                    type="primary",
                    use_container_width=True
                )